    private void btnBuscarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMousePressed
        // TODO add your handling code here:
        BDcamisas.abrir_ConexionCm();
        camisa=BDcamisas.consultar_camisas(txtBuscar.getText());
        BDcamisas.cerrar_ConexionCm();

        if(camisa!=null){
            txtTipoE.setEnabled(true);
            listColorE.setEnabled(true);
            listTallaE.setEnabled(true);
            txtCantidadE.setEnabled(true);

            txtTipoE.setText(camisa.getTipo());
            switch(camisa.getColor()){
                case "Blanco" :
                listColorE.setSelectedIndex(0);
                break;
                case "Negro" :
                listColorE.setSelectedIndex(1);
                break;
                case "Rojo" :
                listColorE.setSelectedIndex(2);
                break;
                case "Cafe" :
                listColorE.setSelectedIndex(3);
                break;
                case "Morado" :
                listColorE.setSelectedIndex(4);
                break;
                case "Azul" :
                listColorE.setSelectedIndex(5);
                break;
                case "Verde" :
                listColorE.setSelectedIndex(6);
                break;

            }
            switch(camisa.getTalla()){
                case "XS" :
                listTallaE.setSelectedIndex(0);
                break;
                case "S" :
                listTallaE.setSelectedIndex(1);
                break;
                case "M" :
                listTallaE.setSelectedIndex(2);
                break;
                case "L" :
                listTallaE.setSelectedIndex(3);
                break;
                case "XL" :
                listTallaE.setSelectedIndex(4);
                break;
                case "XXL" :
                listTallaE.setSelectedIndex(5);
                break;
            }
            txtCantidadE.setText(camisa.getCantidad());

        }else
        JOptionPane.showMessageDialog(this, "Codigo de camisas no encontrado en el Inventario ");

    }//GEN-LAST:event_btnBuscarMousePressed

