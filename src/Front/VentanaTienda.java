    private void btnGuardarNMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarNMousePressed
        // TODO add your handling code here:
        Camisa caux;
            if(listColor.getSelectedValue()==null||CombBoxTalla.getSelectedItem()==null||spnCantidad.getValue()==null){
                JOptionPane.showMessageDialog(this,"Campos incompletos Por favor rellene todos los campos ");
            }else{
                BDcamisas.abrir_ConexionCm();
                caux=BDcamisas.consultar_camisas(txtCodigo.getText());
                BDcamisas.cerrar_ConexionCm();
                if(caux==null){
                camisa.setCantidad(spnCantidad.getValue().toString());
                camisa.setColor(listColor.getSelectedValue());
                camisa.setTalla(CombBoxTalla.getSelectedItem().toString());
                camisa.setTipo(txtATipo.getText());
                camisa.setCodigo(Integer.parseInt(txtCodigo.getText()));
                
                BDcamisas.abrir_ConexionCm();
                BDcamisas.insertar_camisas(camisa);
                BDcamisas.cerrar_ConexionCm();
                limpiar();
                }
                else
                    JOptionPane.showMessageDialog(this, "Por favor Utilizar otro Codigo , este ya esta en Uso");
            }
            
        
        
    }//GEN-LAST:event_btnGuardarNMousePressed

